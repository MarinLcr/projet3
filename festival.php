    <h2 class="trytry">LES FILMS DE PLEIN AIR</h2>
    <h3 class="trytry">LE DEBUT D’UNE GRANDE AVENTURE</h3>
    <p  class="trytrytry">Le mois d'août 2015 restera comme le commencement du grande aventure pour le festival de Film de Plein Air avec le déroulement de sa première édition. Pour sa grande première, le festival se déroulera sur trois jours et réunira près de 3000 personnes. Cette année là, douze films seront sélecionnés et diffusés aux grands publics, dont le très poignant "Plus fort que les bombes" qui restera comme le film marquant de cette première édition.</p>
<p class="trytrytry">Le festival de Film de Plein Air fais le choix de diffusés des films axés autour de 6 thèmes:</p>
<ul>
  <li class="trytrytry">Le drame, dont l'histoire tourne autour de faits tragiques.</li>
  <li class="trytrytry">La romance amoureuse, dont l'histoire met l'emphase sur l'évolution de la relation amoureuse entre les protagonistes.</li>
  <li class="trytrytry">Le film historique, ayant pour vocation de restituer un évènement réel en y étant plus ou moins fidèle.</li>
  <li class="trytrytry">Le film d'anticipation, dont l'action se déroule dans un futur proche ou plus lointain.</li>
  <li class="trytrytry">Le film de portrait, souvent hagiographique et historique, qui développe, au travers des dialogues et des évènements (anodins ou non), la psychologie d'un ou plusieurs personnages.</li>
  <li class="trytrytry">La comédie, ayant pour but de divertir en représentant les ridicules des caractères et des mœurs, d'une société.</li>
</ul>
<p class="trytrytry">Pour l'occasion, la mairie de Paris met à disposition du festival trois sites de la ville afin de pouvoir varier les lieux de diffusions des films. Cette année là, le parc de Villette, le parc Saint-Cloud ainsi que le château de Vincennes seront les trois lieux mis à disposition du festival. </p>
<h3>LA QUATRIEME EDITION</h3>
<p>Avec plus de 40 films diffusés et près de 10000 spectateurs, les trois premières éditions ont clairement rencontrés un franc succès. Pour cette quatrième édition, le Festival du Film de Plein Air à fais le choix d'innover un peu en choisissant un thème pour chacune des soirées du festival. Trois films abordant et répondant à un thème bien précis seront donc diffusés chaque soir, avec pour objectif de mettre en parallèle les différentes oeuvres proposées. </p>
<p>Voici donc les différents thèmes choisis pour cette quatrième édition du festival du Film de Plein Air:</p>
<ul>
  <li>Lundi 5 août 2019: le cinéma et la mer.</li>
  <li>Mardi 6 août 2019: l'éducation à travers le monde.</li>
  <li>Mercredi 7 août 2019: le monde de demain.</li>
  <li>Jeudi 8 août 2019: les dérives des médias.</li>
</ul>
<img class="w-100 my-4" src="http://leclercmarin.fr/projet2/img/cinemapleinr.jpg" alt="New York">
<p>Lors de la Cérémonie de Clôture, deux jurys composés de professionnels, un jury de lycéens de Normandie mais aussi les spectateurs, récompensent parmi les longs métrages : le Grand Prix, le Prix de la Jeunesse et le Prix du Public et parmi la sélection des courts métrages : le meilleur film court, la meilleure actrice et le meilleur acteur.</p>
