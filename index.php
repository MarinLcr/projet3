      <h2 class="mb-3">Les films de plein air</h2>
      <p>Le festival de films de Plein Air est un festival qui se déroule depuis maintenant quatres années au cours du mois d'août dans divers lieux publics de Paris. L'objectif principal de cet événement est de faire découvrir des films de tout horizon, indépendant ou non, à un public qui se veux curieux ou bien connaisseur. Pour chaque édition, une programmation de divers films est concocté par les membres du bureau de l'association organisatrice du festival, avec pour objectif de surprendre et de faire décoivrir de noveaux horizons cinématographiques au grand public.</p>
      <p>Pour chaque édition un jury de différents acteurs présents dans le monde cinématographique est réunis pour décerner le film gagnant de l'éditon. Ce jury peut être composé tant d'acteurs, que de réalisateurs ou encore de producteur. Le but étant vraiment de représenter le monde du cinéma et ces dessous. Lors de ses trois premières éditions le festival à réunis près de 10 000 personnes au total avec près de 9 soirées organisés et 27 films diffusés. Pour sa quatrième édition le festival se déroulera cette fois-ci sur quatres soirs, du 5 au 8 août 2019.</p>
